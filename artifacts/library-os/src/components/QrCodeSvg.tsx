export default function QrCodeSvg({ size = 200, className = "" }: { size?: number; className?: string }) {
  const cell = size / 21;
  return (
    <svg width={size} height={size} viewBox={`0 0 ${size} ${size}`} className={className}>
      <rect width={size} height={size} fill="white" />
      {PATTERN.map((row, r) =>
        row.map((bit, c) =>
          bit ? (
            <rect
              key={`${r}-${c}`}
              x={c * cell}
              y={r * cell}
              width={cell}
              height={cell}
              fill="black"
            />
          ) : null
        )
      )}
    </svg>
  );
}
